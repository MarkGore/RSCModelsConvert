	public static ModelNames getModel(Integer hash) {
		if (getModelNames().size() == 0)
			loadModels();
		for (ModelNames names : getModelNames())
			if (names.getHash() == hash)
				return names;
		return null;
	}
	
	public static int getHash(String modelName) {
		if (getModelNames().size() == 0)
			loadModels();
		for (ModelNames names : getModelNames())
			if (names.getName().equalsIgnoreCase(modelName+".OB3"))
				return names.getHash();
		return -1;
	}

